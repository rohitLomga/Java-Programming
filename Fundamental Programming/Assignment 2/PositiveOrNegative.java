public class PositiveOrNegative {
    public static void main(String[] args){
        int number = 15;

        if(number < 0){
            System.out.println("This is negative number");
        } else if (number > 0) {
            System.out.println("This is positive number");
        }else {
            System.out.println("This is zero");
        }
    }
}
